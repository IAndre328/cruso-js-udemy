/*
    Método log dentro do objeto console
    Caso queira escrever um texto escreva entre "", '' ou ``
    Caso queira escrever uma variável escreva sem as aspas

    Centro de aspas, o texto será uma *string* <- texto
    Só é exibida no console do programa
*/ 

console.log("Olá");
console.log('Olá mundo');
console.log(`André Batista`);

/*
    Caso queira colocar aspas dentro do seu texto, este deve ser diferente da que usou para abrir e fechar o texto
*/

console.log("André 'Batista'");
console.log('André "Batista"');

/*
    Os números sendo number escrevem fora de aspas
*/

console.log(124, 1.3, "André de novo")